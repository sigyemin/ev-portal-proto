/* ============================================================================
   kev100-stat.js — K-EV100 보유·전환 통계 ★공용 상수(단일 소스)
   ----------------------------------------------------------------------------
   [ISS-069] kev100-about.html 요약카드와 kev100-companies.html 도넛이 같은 값을 쓰도록
   두 페이지가 이 파일 하나를 참조한다(두 화면 수치가 어긋나면 안 됨).

   [DEV] 원천 = PS_KEV100 · PS_KEV100_HIS (관리자 'K-EV100 통계 > 전체 현황'과 동일 지표).
         ★집계 범위 = K-EV100 참여(승인) 기업 보유 차량 한정 — 전국 전체 보급 대수 아님.
         수치는 시연용 더미 — 실연동 시 승인 계정 기준 집계로 교체.
   ============================================================================ */
(function (w) {
  'use strict';
  var FLEET = 10600;   // 참여 기업 전체 보유·임차 차량
  var ICE   = 8420;    // 내연기관차
  var ZEV   = 2180;    // 무공해차 (= FLEET - ICE)
  var EV    = 1842;    // 전기자동차
  var H2    = 338;     // 수소전기차 (= ZEV - EV)
  var FIRMS = 524;     // 누적 참여 기업(개사)

  w.KEV100_STAT = {
    firms: FIRMS,
    fleet: FLEET,
    ice: ICE,
    zev: ZEV,
    ev: EV,
    h2: H2,
    // 차종별(승용·승합·화물) — 각 소계는 상위 합계와 일치
    iceByType: { car: 5230, bus: 1640, truck: 1550 },
    evByType:  { car: 1240, bus: 382,  truck: 220  },
    h2ByType:  { car: 198,  bus: 92,   truck: 48   },
    // 전환율(%) = 무공해차 ÷ 전체 보유 · 소수 1자리
    zevRate: function () { return (ZEV / FLEET * 100).toFixed(1); }
  };
})(window);
